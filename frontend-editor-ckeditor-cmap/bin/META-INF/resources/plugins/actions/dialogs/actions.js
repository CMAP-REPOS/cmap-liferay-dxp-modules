CKEDITOR.dialog.add( 'actions', function( editor ) {
  return {
    title: 'Edit Actions',
    minWidth: 200,
    minHeight: 100,
    contents: [
      {
        id: 'info',
        elements: [
          {
            id: 'count',
            type: 'text',
            label: 'Number of items',
            
            setup: function( widget ) {
              this.setValue( widget.element.data('count') );
            },
            commit: function( widget ) {

              // https://ckeditor.com/old/forums/CKEditor/help-Inserting-a-widget-from-my-own-button
              // https://stackoverflow.com/questions/20237845/ckeditor-initialize-widget-added-with-insertelement?rq=1
              // var row = .findOne('.row');
              for(var i=0; i<this.getValue(); i++){  

                var element = CKEDITOR.dom.element.createFromHtml('<div class="actions-row row">' +
                  '<div class="col-sm-10">' +
                    '<p>Plan for infrastructure needs of the whole community through a capital improvement plan, including an assessment of the long-term maintenance costs generated by existing and planned developments</p>' +
                  '</div>' +
                  '<div class="col-sm-6">' +
                    '<p>Local governments</p>' +
                  '</div>' +
                  '<div class="col-sm-16">' +
                    '<hr/>' +
                  '</div>' +
                '</div>', editor.document );
                
                widget.element.append(element);
                // var w = editor.widgets.initOn( element, 'page-card' );
                // editor.widgets.checkWidgets()
                // console.log(w);
              }
              editor.widgets.checkWidgets()
              widget.element.data('count', this.getValue());

            }
          }
        ]
      }
    ]
  };
});